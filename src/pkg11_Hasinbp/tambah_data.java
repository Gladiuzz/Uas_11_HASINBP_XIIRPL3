   void ShowData(String id_inventaris){
        try{
            Statement stmt = connection.createStatement();
            String query = "SELECT * FROM inventaris WHERE id_inventaris = '"+id_inventaris+"'";
            ResultSet rs = stmt.executeQuery(query);
            rs.next();

            
            txt_nama.setText(rs.getString("nama"));
            txt_kondisi.setText(rs.getString("kondisi"));
            cb_keterangan.setSelectedItem(rs.getString("keterangan"));
            txt_jumlah.setText(rs.getString("jumlah"));
            cb_jenis.setSelectedItem(rs.getString("jenis"));
            txt_tgl.setText(rs.getString("tanggal"));
            cb_ruang.setSelectedItem(rs.getString("ruang"));
            cb_petugas.setSelectedItem(rs.getString("petugas"));
             

        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan di query");
        }

    }
   
