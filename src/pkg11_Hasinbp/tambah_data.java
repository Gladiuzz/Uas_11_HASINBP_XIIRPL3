   public void insertdata(){
       String id_inventaris = txt_idInventaris.getText();
       String nama = txt_nama.getText();
       String Kondisi = txt_kondisi.getText();
       String keterangan = cb_keterangan.getSelectedItem().toString();
       String jumlah = txt_jumlah.getText();
       String jenis = cb_jenis.getSelectedItem().toString();
       String tanggal = txt_tgl.getText();
       String ruang = cb_ruang.getSelectedItem().toString();
       String petugas = cb_petugas.getSelectedItem().toString();
       
       try {
           String query = "INSERT INTO `inventaris` (`id_inventaris`, `nama`,`kondisi`,`keterangan`,`jumlah`,`id_jenis`,`tanggal_register`,`id_ruang`,`id_petugas`)"
                   + "VALUES ('"+id_inventaris+"','"+nama+"',"
                   + "'"+Kondisi+"','"+keterangan+"','"+jumlah+"','"+jenis+"','"+tanggal+"',"
                   + "'"+ruang+"','"+petugas+"');";
           stmt = connection.createStatement();
           int i = stmt.executeUpdate(query);
           System.out.println(query);
           
           if (i == 1) {
               JOptionPane.showMessageDialog(this, "Data Berhasil Dimasukkan");
               Data_inventaris invent = new Data_inventaris();
               invent.viewInventaris();
           }
           else{
               JOptionPane.showMessageDialog(this, "Data gagal dimasukkan");
           }
       } catch (Exception e) {
           e.printStackTrace();
           JOptionPane.showMessageDialog(this, "koneksi gagal");
       }
       
   }
   
   public void addKeterangan(){

       try {
            String query = "SELECT * FROM jenis";
            stmt = connection.createStatement();
            rs = stmt.executeQuery(query);   
            
            while (rs.next()) {
                String item = rs.getString("keterangan");
                cb_keterangan.addItem(item);
           }
       } catch (SQLException e) {
           System.out.println("Koneksi Gagal" + e.getMessage());
       }
   }
   
